package conditionalStats;

public class SwitchDemo {

	public static void main(String[] args) {

		int n = 100;

		switch (n) {
		default:
			System.out.println("end");
			break;
		case 'a':
			System.out.println("ten");
			break;
		case 'b':
			System.out.println("twenty");
			break;
		case 'c':
			System.out.println("thirty");
			break;

		}
	}

}
