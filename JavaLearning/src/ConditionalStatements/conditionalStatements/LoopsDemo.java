package conditionalStats;

public class LoopsDemo {
	public static void main(String args[]) {
		
		int x = 10;

		/* While Statement */
		while (x < 20) {
			System.out.print("value of x : " + x);
			x++;
			System.out.print("\n");
		}

		/* Do-While Statement */
		do {
			System.out.print("value of x : " + x);
			x++;
			System.out.print("\n");
		} while (x < 20);
	}

}
